def backup(args):
    bakpath, fullname = args
    # MD5
    m = md5(fullname)
    f_md5 = m.hexdigest()
    path = os.path.join(bakpath, f_md5[:2])
    try:
        if not os.access(path, os.F_OK):
            os.makedirs(path)
            logging.info(u' %s' %path)
    except Exception as err:
        logging.error(u' %s ' % path)
        logging.debug(err)
        return False
    # MD5
    logging.info(u' %s  %s' % (fullname, os.path.join(path, f_md5)))
    if not os.access(fullname, os.R_OK):
        logging.error(u' %s ' % fullname)
        return False
    if not os.access(path, os.W_OK):
        logging.error(u' %s ' % path)
        return False
    fp = open(fullname, 'rb')
    data = fp.read()
    fp.close()
    m = md5(data)
    fp = open(os.path.join(path, f_md5), 'wb')
    fp.write(pack('I', len(m.hexdigest())))
    fp.write(m.hexdigest())
    fp.write(pack('I', len(fullname)))
    fp.write(fullname)
    fp.write(pack('I', len(data)));
    fp.write(data)
    fp.flush()
    fp.close()
    logging.info(u' %s  %s ' % (fullname, os.path.join(path, f_md5)))
    return True

